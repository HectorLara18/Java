public class SwitchCase {
    public static void main(String[] args) {
        String usuario = "hlara25";
        switch(usuario) {
            case "admin":
            System.out.println("Acceso Correcto!");
            break;
            case "hlara":
            System.out.println("Acceso Correcto!");
            break;
            default:
            System.out.println("Acceso Denegado!!");
        } 
    }
}
