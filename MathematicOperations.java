public class MathematicOperations {
    public static void main(String[] args) {
        double x = 2.1;
        double y = 3;
        System.out.println(Math.ceil(x));
        System.out.println(Math.floor(x));
        System.out.println(Math.pow(x, y));
        
        int xInt = (int) x;
        System.out.println(xInt); 
    }
    
}
