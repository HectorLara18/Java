public class CicloIf {
    public static void main(String[] args) {
        int x = 1234;
        int y = 1234;
        int z = 0;
        if(x == y){
            System.out.println("Los valores son iguales");
            z++;
        } else {
            System.out.print("El valor es diferente");
        }
        System.out.println(z);
    }

    
}
